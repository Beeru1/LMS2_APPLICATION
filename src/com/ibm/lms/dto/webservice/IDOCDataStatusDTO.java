package com.ibm.lms.dto.webservice;

public class IDOCDataStatusDTO {
	
	private String prospectsMobileNumber; // msidn no
	private String alternateContactNumber; //value  to be as 1234567891,9876543210
	private String onlineCafNo;
	private String status;
	private String status_date;
	private String rejectReason;
	private String idocStep;
	private String source;
	
	
	
	public String getProspectsMobileNumber() {
		return prospectsMobileNumber;
	}
	public void setProspectsMobileNumber(String prospectsMobileNumber) {
		if(prospectsMobileNumber !=null && prospectsMobileNumber.length() > 30) {
			this.prospectsMobileNumber = prospectsMobileNumber.substring(0, 29);
		}else {
			this.prospectsMobileNumber = prospectsMobileNumber;	
		}
	}
	public String getAlternateContactNumber() {
		return alternateContactNumber;
	}
	public void setAlternateContactNumber(String alternateContactNumber) {
		if(alternateContactNumber !=null && alternateContactNumber.length() > 50) {
			this.alternateContactNumber = alternateContactNumber.substring(0, 49);
		}else {
			this.alternateContactNumber = alternateContactNumber;	
		}
		
	}
	public String getOnlineCafNo() {
		return onlineCafNo;
	}
	public void setOnlineCafNo(String onlineCafNo) {
		if(onlineCafNo !=null && onlineCafNo.length() > 30) {
			this.onlineCafNo  = onlineCafNo.substring(0, 29);
		}else {
			this.onlineCafNo = onlineCafNo;
		}
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		if(status !=null && status.length() > 20) {
			this.status  = status.substring(0, 19);
		}else {
			this.status = status;
		}
	}
	public String getStatus_date() {
		return status_date;
	}
	public void setStatus_date(String status_date) {
		if(status_date !=null && status_date.length() > 15) {
			this.status_date  = status_date.substring(0, 14);
		}else {
			this.status_date = status_date;
		}
	}
	public String getRejectReason() {
		return rejectReason;
	}
	public void setRejectReason(String rejectReason) {
		if(rejectReason !=null && rejectReason.length() > 1000) {
			this.rejectReason  = rejectReason.substring(0, 999);
		}else {
			this.rejectReason = rejectReason;
		}
	}
	public String getIdocStep() {
		return idocStep;
	}
	public void setIdocStep(String idocStep) {
		if(idocStep !=null && idocStep.length() > 30) {
			this.idocStep  = idocStep.substring(0, 29);
		}else {
			this.idocStep = idocStep;
		}
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		if(source !=null && source.length() > 25) {
			this.source  = source.substring(0, 24);
		}else {
			this.source = source;
		}
	}

	
	
}
